export function translate(
  locale: AppLocale,
  key: TranslationKey,
  vars?: Record<string, string | number>
): string {
  const table = locale === 'es' ? ES : EN;
  let raw: string = table[key] ?? EN[key];
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      raw = raw.replaceAll(`{${k}}`, String(v));
    }
  }
  return raw;
}
